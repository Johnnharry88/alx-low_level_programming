#include <stdio.h>
/**
 * div - function that returns the quotient of two int
 * @a: first integer
 * @b: second integer
 * Return: quotient of twon integers
 */
int div(int a, int b)
{
	return (a / b);
}

/** 
 * mod - function that return the fraction of two int.
 * @a: first integer
 * @b: second integer
 * Return: Remainder of two integers.
 */
int mod(int a , int b)
{
	return(a % b);
}
